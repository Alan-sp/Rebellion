function openPopup(){
    var blur=document.getElementById('blur');
    blur.classList.add('active');
    var popup=document.getElementById('popup');
    popup.classList.add('active');
}
function closePopup(){
    var blur=document.getElementById('blur');
    blur.classList.remove('active');
    var popup=document.getElementById('popup');
    popup.classList.remove('active');
}
